import Head from "next/head";
import groq from "groq";
import client from "../../client-config";

import DefaultTemplate from "../../components/Default-Template/default-template";

export default function Project({ project }) {
  return (
    <>
      <Head>
        <title>Tim Nees Architects</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <DefaultTemplate data={project}></DefaultTemplate>
    </>
  );
}

export async function getStaticPaths() {
  const paths = await client.fetch(
    groq`*[_type == "projects" && defined(slug.current)][].slug.current`
  );

  return {
    paths: paths.map((slug) => ({ params: { slug } })),
    fallback: true,
  };
}

export async function getStaticProps(context) {
  const { slug = "" } = context.params;
  const project = await client.fetch(
    groq`*[_type == "projects" && slug.current == "${slug}"]`
  );

  return {
    props: {
      project: project[0],
    },
  };
}
